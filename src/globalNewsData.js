// Helper function to get all sources by continent
export const getSourcesByContinent = () => {
  const continents = {};
  GLOBAL_NEWS_SOURCES.forEach(country => {
    if (!continents[country.continent]) {
      continents[country.continent] = [];
    }
    continents[country.continent].push(country);
  });
  return continents;
};

// Helper function to search sources
export const searchSources = (query) => {
  const searchTerm = query.toLowerCase();
  return GLOBAL_NEWS_SOURCES.filter(country => 
    country.name.toLowerCase().includes(searchTerm) ||
    country.sources.some(source => 
      source.name.toLowerCase().includes(searchTerm) ||
      source.category.toLowerCase().includes(searchTerm)
    )
  );
};

// Get all live streams
export const getLiveStreams = () => {
  const liveStreams = [];
  GLOBAL_NEWS_SOURCES.forEach(country => {
    country.sources.forEach(source => {
      if (source.streamUrl) {
        liveStreams.push({
          ...source,
          country: country.name,
          flag: country.flag
        });
      }
    });
  });
  return liveStreams;
};

// Get all website-only sources
export const getWebsiteSources = () => {
  const websites = [];
  GLOBAL_NEWS_SOURCES.forEach(country => {
    country.sources.forEach(source => {
      if (!source.streamUrl && source.websiteUrl) {
        websites.push({
          ...source,
          country: country.name,
          flag: country.flag
        });
      }
    });
  });
  return websites;
};
